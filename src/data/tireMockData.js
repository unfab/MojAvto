// Helper: get tire by ID
export function getTireById(id) {
    return MOCK_TIRES.find(t => t.id === id) || null;
}

// Helper: search/filter tires
export function searchTires({ width, height, diameter, season, brands, priceFrom, priceTo, speedRatings, fuelFrom, wetGripFrom }) {
    return MOCK_TIRES.filter(tire => {
        if (width && tire.width !== parseInt(width)) return false;
        if (height && tire.height !== parseInt(height)) return false;
        if (diameter && tire.diameter !== parseInt(diameter)) return false;
        if (season && season !== 'vse' && tire.season !== season) return false;
        if (brands && brands.length > 0 && !brands.includes(tire.brand)) return false;
        if (priceFrom && tire.lowestPrice < parseFloat(priceFrom)) return false;
        if (priceTo && tire.lowestPrice > parseFloat(priceTo)) return false;
        if (speedRatings && speedRatings.length > 0 && !speedRatings.includes(tire.speedRating)) return false;
        if (fuelFrom && tire.euLabel.fuelEfficiency > fuelFrom) return false;
        if (wetGripFrom && tire.euLabel.wetGrip > wetGripFrom) return false;
        return true;
    });
}
